    private void jListTelValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jListTelValueChanged
        // TODO add your handling code here:
        //el metodo getValuechange filtra los eventos a travÃ©s de getValuesAdjusting() 
        
        if(evt.getValueIsAdjusting()) return; //ignoramos eventos intermedios 
        //o sea se evita duplicar llamadas miestras se arrastra o selecciona con teclado 

        //validar datos vacios 
        Long tel = jListTel.getSelectedValue(); 
        if(tel == null) { 
            JOptionPane.showMessageDialog(this, "Debe selecionar un telÃ©fono","ERROR",JOptionPane.ERROR_MESSAGE); 
            return; 
        } 
        Contacto c = Gestion.buscarContacto(tel);//llamamos al metodo para que nos devuelva el contacto asociado al telefono pasado por parametro 
        if(c == null){ 
            JOptionPane.showMessageDialog(this, "NingÃºn contacto estÃ¡ asociado a ese Telefono","ERROR",JOptionPane.ERROR_MESSAGE); 
            return; 
        } 
        
        jTextFieldApellido.setText(c.getApellido()); 
        jTextFieldNombre.setText(c.getNombre()); 
        jTextFieldDNI.setText(String.valueOf(c.getDni())); 
        jTextFieldDireccion.setText(c.getDireccion());
        jTextFieldCiudad.setText(String.valueOf(c.getCiudad()));
    }//GEN-LAST:event_jListTelValueChanged

    private void jTextFieldTelefonoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldTelefonoKeyReleased
        // TODO add your handling code here:
        String telStr = jTextFieldTelefono.getText();
        DefaultListModel<Long> mod = new DefaultListModel<>();
        if(!telStr.isEmpty()){
            try{
                for (Long t : Gestion.listarTelefonos()) {
                    if(String.valueOf(t).startsWith(telStr)){
                        mod.addElement(t);
                    }
                }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this,"Debe ser un valor numÃ©rico","ERROR",JOptionPane.ERROR_MESSAGE);
            }
        }
        jListTel.setModel(mod);
        
    }//GEN-LAST:event_jTextFieldTelefonoKeyReleased
 

