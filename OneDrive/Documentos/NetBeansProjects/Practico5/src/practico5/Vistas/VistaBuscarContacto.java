    private void llenarListaTelefonos(){
        DefaultListModel<Long> modelo = new DefaultListModel<>();
        Set<Long> telefonos = Gestion.listarTelefonos();
        for(Long tel : telefonos){
            modelo.addElement(tel);
        }
        jListTel.setModel(modelo);
    }
    private void llenarDatosContacto(long telefonoContacto){
        Contacto cont = Gestion.buscarContacto(telefonoContacto);
        
        jTextFieldDNI.setText(String.valueOf(cont.getDni()));
        jTextFieldApellido.setText(String.valueOf(cont.getApellido()));
        jTextFieldNombre.setText(String.valueOf(cont.getNombre()));
        jTextFieldCiudad.setText(String.valueOf(cont.getCiudad()));
        jTextFieldDireccion.setText(String.valueOf(cont.getDireccion()));
    }


